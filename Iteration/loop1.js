//while & do while loop

let i = 0;
while (i <= 10) {
    console.log(`value of i is ${i}`);
    i++;
}


let arr = [1,2,3,4,6];
let j = 0;
while(j < arr.length){
    console.log(`value is ${arr[j]}`);
    j++;
}

//do while

let score = 11;
do {
    console.log(`score is ${score}`);
    score++;
} while (score <= 10);